// ── 핵심 해석 생성 함수 ───────────────────────────────────────
/**
 * @param {string}   id           - 검사 ID
 * @param {number}   pct          - 전체 점수 (0~100)
 * @param {object}   level        - LEVELS 항목
 * @param {string[]} groupLabels  - 그룹 레이블 배열
 * @param {number[]} groupScores  - 그룹별 점수 (0~100)
 * @returns {string}              - 완성된 해석 텍스트
 */
export function generateInterpretation({ id, pct, level, groupLabels, groupScores }) {
  const levelIdx = LEVELS.indexOf(level);

  // 1) 기본 문장
  const baseParagraph = pick(BASE_SENTENCES[id]?.[levelIdx] ?? ['현재 상태를 확인했습니다.']);

  // 2) 그룹 점수 분석 — 가장 높은(심각) / 가장 낮은(양호) 영역 추출
  const sorted = groupLabels
    .map((label, i) => ({ label, score: groupScores[i] }))
    .sort((a, b) => b.score - a.score);

  const worst = sorted[0];   // 점수 가장 높은 = 가장 심각한 그룹
  const best  = sorted[sorted.length - 1]; // 점수 가장 낮은 = 가장 양호한 그룹

  const parts = [baseParagraph];

  // 3) 심각 그룹 언급 — 정상 범위에서도 그룹 차이가 크면 언급
  const worstBank = GROUP_BANK[worst.label];
  if (worstBank && worst.score >= 20) {
    parts.push(pick(worstBank.high));
  }

  // 4) 양호 그룹 언급 — 두 그룹이 다르고 best가 충분히 낮을 때
  const bestBank = GROUP_BANK[best.label];
  if (bestBank && best.label !== worst.label && best.score <= 40) {
    parts.push(pick(bestBank.low));
  }

  // 5) 점수 수치 코멘트 — 중간/심각 수준에서 추가
  if (levelIdx >= 2) {
    const numComments = [
      `현재 점수는 ${pct}점으로, 적극적인 관심과 지원이 필요한 수준입니다.`,
      `전체 점수 ${pct}점은 전문가의 도움을 받아볼 필요가 있는 수준입니다.`,
    ];
    parts.push(pick(numComments));
  }

  // 6) 조언 문장
  const advice = pick(ADVICE_BANK[id]?.[levelIdx] ?? []);
  if (advice) parts.push(advice);

  return parts.join(' ');
}