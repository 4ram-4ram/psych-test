// ── 점수 계산 ─────────────────────────────────────────────────
const WEIGHTS   = { avg: 0.6, total: 0.4 };
const MAX_SCORE = 5;

export function calcResult(scores, maxTotal) {
  const n     = scores.length;
  const total = scores.reduce((s, v) => s + v, 0);
  const avg   = total / n;

  const variance = scores.reduce((s, v) => s + (v - avg) ** 2, 0) / n;
  const std = Math.sqrt(variance);

  const normAvg   = avg   / MAX_SCORE;
  const normTotal = total / maxTotal;

  const weighted = normAvg * WEIGHTS.avg + normTotal * WEIGHTS.total;
  const pct = Math.min(100, Math.round(weighted * 100));

  return {
    pct,
    total: Math.round(total * 10) / 10,
    avg:   parseFloat(avg.toFixed(2)),
    std:   parseFloat(std.toFixed(2)),
  };
}

export function getLevel(pct) {
  return LEVELS.find(l => pct <= l.max) ?? LEVELS[LEVELS.length - 1];
}

// ── 유틸 ──────────────────────────────────────────────────────
function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

